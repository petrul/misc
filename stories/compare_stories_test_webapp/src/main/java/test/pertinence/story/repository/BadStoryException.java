package test.pertinence.story.repository;

public class BadStoryException extends Exception {

	private static final long serialVersionUID = 1L;

	public BadStoryException() {
		super();
	}

	public BadStoryException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public BadStoryException(String arg0) {
		super(arg0);
	}

	public BadStoryException(Throwable arg0) {
		super(arg0);
	}

}
